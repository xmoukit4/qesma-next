export type GroupCategory = keyof typeof defaultGroupCategories;
export type ExpenseCategory = keyof (typeof defaultExpenseCategories)[GroupCategory];

export interface Group {
  id: string;
  name: string;
  description?: string;
  category: GroupCategory;
  members: string[];
  createdBy: string;
  createdAt: Date;
}

export interface Expense {
  id: string;
  description: string;
  amount: number;
  currency: string;
  paidBy: string;
  split: 'equally' | 'custom';
  customSplit?: { [key: string]: number };
  members: string[];
  groupId: string;
  category: ExpenseCategory;
  receipt?: string;
  createdAt: Date;
  tags?: string[];
}

export interface Debt {
  from: string; // User ID
  to: string; // User ID
  amount: number;
  currency: string;
  groupId: string;
}
